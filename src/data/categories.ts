export const getRandomFallbackWord = (difficulty: 'easy' | 'normal' | 'hard' = 'normal') => {
    // Filter categories by difficulty, or fall back to all if none match
    let filtered = FALLBACK_CATEGORIES.filter(c => c.difficulty === difficulty);
    if (filtered.length === 0) {
        filtered = FALLBACK_CATEGORIES;
    }

    const randomCategory = filtered[Math.floor(Math.random() * filtered.length)];
    const randomWordData = randomCategory.words[Math.floor(Math.random() * randomCategory.words.length)];
    return {
        word: randomWordData.word,
        category: randomCategory.name,
        hint: randomWordData.hint,
    };
};
